package org.example;

public class Cars {
    private int no_wheels;
    private int no_sheets;
    private String color;

    public int getNo_wheels() {
        return no_wheels;
    }

    public void setNo_wheels(int no_wheels) {
        this.no_wheels = no_wheels;
    }

    public int getNo_sheets() {
        return no_sheets;
    }

    public void setNo_sheets(int no_sheets) {
        this.no_sheets = no_sheets;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
